package Registration;

public class ContactInformation {
	String firstName;
	String lastName;
	String phone;
	String email;
	
	public String getFirstName() {
		return firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public String getPhone() {
		return phone;
	}
	public String getEmail() {
		return email;
	}

}
